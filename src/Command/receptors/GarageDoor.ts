export default class GarageDoor {
  public up(): void {
    console.log('Garage Door Up');
  }

  public down(): void {
    console.log('Garage Door Down');
  }

  public stop(): void {
    console.log('Garage Door Stoped');
  }

  public lightOn(): void {
    console.log('Garage Door Light On');
  }

  public lightOff(): void {
    console.log('Garage Door Light Off');
  }
}
